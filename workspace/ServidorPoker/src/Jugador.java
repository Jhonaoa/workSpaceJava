
public class Jugador {

	private int puesto;
	private int dinero;
	private boolean activo;
	
	
	public Jugador()
	{
		puesto = 0;
		dinero = 0;
		activo = true;
		
	}
	
	public void setPuesto(int p)
	{
		puesto = p;
	}
	
	public void setDinero(int d)
	{
		dinero = d;
	}
	
	public void setActivo(boolean a)
	{
		activo = a;
	}


	

}

